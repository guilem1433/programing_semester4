public class Autor {
    private int id;
    private String nombre;
    private int nacimiento;
    private String nacionalidad;

    public Autor(int id,
                     String nombre,
                     int nacimiento,
                     String nacionalidad)

    {
        this.id = id;
        this.nombre = nombre;
        this.nacimiento = nacimiento;
        this.nacionalidad = nacionalidad ;
    }
}
