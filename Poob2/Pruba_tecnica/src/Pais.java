public class Pais {
    private int id;
    private String nombre;
    private String continente;
    private String lengua;

    public Pais(int id,
                     String nombre,
                     String continente,
                     String lengua)
    {
        this.id = id;
        this.nombre = nombre;
        this.continente = continente;
        this.lengua = lengua;
    }
}
